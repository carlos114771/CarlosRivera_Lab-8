    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        // TODO add your handling code here:
        JFileChooser chooser = new JFileChooser();
        int contador = chooser.showOpenDialog(this);
        File file = chooser.getSelectedFile();
        try {
            FileInputStream fw = new FileInputStream(file);
            ObjectInputStream bw = new ObjectInputStream(fw);
            Hadas temporal;
            try {
                while ((temporal = (Hadas) bw.readObject()) != null) {
                    if (temporal instanceof Lamias) {
                        lamias.add((Lamias) temporal);
                    } else if (temporal instanceof Hamadriades) {
                        hamadriades.add((Hamadriades) temporal);
                    } else if (temporal instanceof Silfides) {
                        silfides.add((Silfides) temporal);
                    } else if (temporal instanceof Salamandras) {
                        salamandras.add((Salamandras) temporal);
                    }
                }
                archivo = file;
            } catch (Exception e) {
            } finally {
                bw.close();
                fw.close();
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jButton2MouseClicked

    private void jButton4MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton4MouseClicked
        // TODO add your handling code here:
        if (cont != 0) {
            JFileChooser jfc = new JFileChooser();
            int selecion = jfc.showSaveDialog(this);
            if (selecion == JFileChooser.APPROVE_OPTION) {
                direccion = jfc.getSelectedFile();
            }
            cont++;
        }
        try {
            FileOutputStream salida = new FileOutputStream(direccion);
            ObjectOutputStream salida2 = new ObjectOutputStream(salida);
            salida2.writeObject(lamias);
            salida2.writeObject(hamadriades);
            salida2.writeObject(silfides);
            salida2.writeObject(salamandras);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton4MouseClicked

